<template>
  <validation-observer ref="observer" v-slot="{ invalid }">
    <v-overlay :value="overlay">
      <v-progress-circular indeterminate size="64"></v-progress-circular>
    </v-overlay>
    <v-alert
      v-model="resetAlert"
      class="ma-5"
      :value="alert"
      border="bottom"
      :type="alert_type"
      transition="scale-transition"
      dismissible
      >{{ alert_body }}
    </v-alert>
    <v-card class="pa-8 ma-5" color="primary" elevation="5">
      <div class="d-flex flex-column">
        <v-icon color="white" size="50"> mdi-home-heart </v-icon>
        <h1 style="text-align: center; color: white">
          Agregar un nuevo Centro
        </h1>
      </div>
    </v-card>
    <v-card class="pa-8 ma-5" elevation="5">
      <div class="d-flex flex-column">
        <v-icon size="45" color="primary">mdi-clipboard-edit</v-icon>
        <h3 class="pb-5" style="text-align: center; color: primary">
          Por favor, completá los siguientes datos
        </h3>
      </div>

      <v-divider class="mb-10"></v-divider>
      <form id="test_form" @submit.prevent="submit">
        <validation-provider
          v-slot="{ errors }"
          name="nombre"
          rules="required|max:50"
        >
          <v-text-field
            v-model="nombre"
            :counter="50"
            :error-messages="errors"
            label="Nombre"
            name="nombre"
            prepend-icon="mdi-text"
            required
          ></v-text-field>
        </validation-provider>
        <v-text-field
          v-model="web"
          :error-messages="errors"
          label="Sitio Web (Opcional)"
          name="sitio_web"
          prepend-icon="mdi-web"
          required
        ></v-text-field>
        <validation-provider
          v-slot="{ errors }"
          name="teléfono"
          rules="required|numeric"
        >
          <v-text-field
            v-model="telefono"
            :error-messages="errors"
            label="Teléfono"
            name="telefono"
            prepend-icon="mdi-phone"
            required
          ></v-text-field>
        </validation-provider>
        <validation-provider v-slot="{ errors }" name="tipo" rules="required">
          <v-text-field
            v-model="tipo"
            :error-messages="errors"
            label="Tipo (Ropa, Alimentos, Sangre, etc)"
            name="tipo"
            prepend-icon="mdi-text-box"
            required
          ></v-text-field>
        </validation-provider>
        <v-menu
          ref="menu_apertura"
          v-model="menu_apertura"
          :close-on-content-click="false"
          :nudge-right="40"
          :return-value.sync="time_apertura"
          transition="scale-transition"
          offset-y
          max-width="290px"
          min-width="290px"
        >
          <template v-slot:activator="{ on, attrs }">
            <validation-provider
              v-slot="{ errors }"
              name="Hora de Apertura"
              rules="required"
            >
              <v-text-field
                v-model="time_apertura"
                label="Hora de Apertura"
                name="hora_apertura"
                prepend-icon="mdi-clock-time-four-outline"
                readonly
                v-bind="attrs"
                v-on="on"
                required
                :error-messages="errors"
              ></v-text-field>
            </validation-provider>
          </template>
          <v-time-picker
            v-if="menu_apertura"
            v-model="time_apertura"
            format="24hr"
            :allowed-minutes="allowedStep"
            min="9:00"
            :max="time_cierre"
            full-width
            @click:minute="$refs.menu_apertura.save(time_apertura)"
          ></v-time-picker>
        </v-menu>
        <v-menu
          ref="menu_cierre"
          v-model="menu_cierre"
          :close-on-content-click="false"
          :nudge-right="40"
          :return-value.sync="time_cierre"
          transition="scale-transition"
          offset-y
          max-width="290px"
          min-width="290px"
        >
          <template v-slot:activator="{ on, attrs }">
            <validation-provider
              v-slot="{ errors }"
              name="Hora de Cierre"
              rules="required"
            >
              <v-text-field
                v-model="time_cierre"
                label="Hora de Cierre"
                name="hora_cierre"
                prepend-icon="mdi-clock-time-four-outline"
                readonly
                v-bind="attrs"
                v-on="on"
                required
                :error-messages="errors"
              ></v-text-field>
            </validation-provider>
          </template>
          <v-time-picker
            v-if="menu_cierre"
            v-model="time_cierre"
            format="24hr"
            :allowed-minutes="allowedStep"
            :min="time_apertura"
            max="16:00"
            full-width
            @click:minute="$refs.menu_cierre.save(time_cierre)"
          ></v-time-picker>
        </v-menu>
        <validation-provider
          v-slot="{ errors }"
          name="email"
          rules="required|email"
        >
          <v-text-field
            v-model="email"
            :error-messages="errors"
            label="E-mail"
            name="email"
            prepend-icon="mdi-at"
            required
          ></v-text-field>
        </validation-provider>
        <validation-provider
          v-slot="{ errors }"
          name="municipio"
          rules="required"
        >
          <v-select
            v-model="select"
            :items="items"
            item-text="mun_nombre"
            item-value="mun_nombre"
            :error-messages="errors"
            label="Municipio"
            name="municipio"
            id="municipio"
            data-vv-name="select"
            prepend-icon="mdi-home-map-marker"
            required
          ></v-select>
        </validation-provider>
        <validation-provider
          v-slot="{ errors }"
          name="direccion"
          rules="required"
        >
          <v-text-field
            v-model="direccion"
            :error-messages="errors"
            label="Dirección"
            name="direccion"
            prepend-icon="mdi-map-marker"
            required
          ></v-text-field>
        </validation-provider>
        <div class="d-flex flex-row ml-10">
          <v-alert type="info" dense>
            Elegí un municipio, ingresá una dirección y hacé click en
            <b>Ver en Mapa</b>
          </v-alert>
          <v-btn class="ml-10 mb-5" color="primary" @click="verEnMapa">
            Ver en Mapa
          </v-btn>
        </div>

        <v-alert
          v-model="resetAlertMapa"
          :value="alertMapa"
          class="ml-10"
          style="width: 50%"
          type="warning"
          transition="scale-transition"
          dismissible
          dense
          border="bottom"
        >
          Ups, no encontramos esa dirección. Revisala e intentá de nuevo.
        </v-alert>
        <v-card class="ml-10">
          <div>
            <l-map
              @click="agregarMarker"
              :zoom="zoom"
              :center="center"
              ref="mapaCentro"
              style="height: 450px; width: 100%; z-index: 1"
            >
              <l-tile-layer :url="url" :attribution="attribution" />
              <l-marker :lat-lng="marker">
                <l-tooltip :options="{ permanent: true, interactive: true }">
                  <div>
                    Esta sería la ubicación del Centro.<br />
                    Si es errónea, simplemente<br />
                    hacé click en la ubicación correcta<br />
                    y se guardará
                  </div></l-tooltip
                >
              </l-marker>
            </l-map>
            <v-overlay :absolute="true" :value="overlayMapa">
              <v-progress-circular
                indeterminate
                size="64"
              ></v-progress-circular>
            </v-overlay>
          </div>
        </v-card>

        <v-file-input
          accept="application/pdf"
          label="PDF Visita"
          name="path_pdf"
        ></v-file-input>

        <v-btn class="mr-4" type="submit" :disabled="invalid" color="primary">
          enviar
        </v-btn>
      </form>
    </v-card>
  </validation-observer>
</template>


<script>
import "leaflet/dist/leaflet.css";
import { latLng } from "leaflet";
import { LMap, LTileLayer, LMarker, LTooltip } from "vue2-leaflet";
import { required, email, max, numeric } from "vee-validate/dist/rules";
import {
  extend,
  ValidationObserver,
  ValidationProvider,
  setInteractionMode,
} from "vee-validate";

setInteractionMode("eager");

extend("required", {
  ...required,
  message: "El campo {_field_} no puede estar vacío",
});

extend("max", {
  ...max,
  message: "El campo {_field_} no puede tener más de {length} caracteres",
});

extend("email", {
  ...email,
  message: "Email inválido",
});

extend("numeric", {
  ...numeric,
  message: "El campo {_field_} solo puede contener números",
});

export default {
  components: {
    ValidationProvider,
    ValidationObserver,
    LMap,
    LTileLayer,
    LMarker,
    LTooltip,
  },
  data() {
    return {
      // Datos Leaflet
      zoom: 6,
      center: [-37.3121792, -61.3996217],
      url: "https://{s}.tile.openstreetmap.org/{z}/{x}/{y}.png",
      attribution:
        '&copy; <a href="http://osm.org/copyright">OpenStreetMap</a> contributors',
      marker: latLng(0, 0),
      // Datos Alerta
      resetAlert: false,
      alert: false,
      alert_type: "success",
      alert_body: "",
      resetAlertMapa: false,
      alertMapa: false,

      // Loading animacion
      overlay: false,
      overlayMapa: false,

      // Datos formulario
      nombre: "",
      telefono: "",
      direccion: "",
      email: "",
      web: "",
      tipo: "",
      select: null,
      time_apertura: null,
      time_cierre: null,
      menu_apertura: false,
      menu_cierre: false,
      errors: null,
      items: [],
      checkbox: null,
    };
  },

  created() {
    this.fetchMunicipios();
  },
  mounted() {
    window.scrollTo({
      top: 0,
      left: 0,
      behavior: "smooth",
    });
  },
  methods: {
    // Función que trae los municipios para llenar el Select
    fetchMunicipios() {
      fetch(
        "https://api-referencias.proyecto2020.linti.unlp.edu.ar/municipios?page=1&per_page=135"
      )
        .then((res) => res.json())
        .then((data) => {
          for (var municipio in data.data.Town) {
            this.items.push({
              mun_id: data.data.Town[municipio].id,
              mun_nombre: data.data.Town[municipio].name,
            });
          }
        });
    },

    // Submit del formulario junto a sus chequeos + Alertas
    submit() {
      this.$refs.observer.validate();
      this.overlay = true;
      const form_data = new FormData(document.getElementById("test_form"));
      fetch("http://127.0.0.1:5000/api/centros", {
        method: "POST",
        body: form_data,
      })
        .then((res) => res.json())
        .then((data) => {
          if (data.status === "201 Created") {
            this.overlay = false;
            this.alert_type = "success";
            this.alert_body =
              "Solicitud de Centro aceptada exitosamente. Estará pendiente de aprobación.";
            this.resetAlert = true;
            this.alert = true;
            window.scrollTo({
              top: 0,
              left: 0,
              behavior: "smooth",
            });
          } else {
            this.overlay = false;
            window.scrollTo({
              top: 0,
              left: 0,
              behavior: "smooth",
            });
            this.alert_type = "error";
            this.alert_body =
              "Hubo un error procesando tu solicitud. Por favor, intentá de nuevo.";
            this.resetAlert = true;
            this.alert = true;
            console.log(data);
            console.log("Hubo un error");
          }
        });
    },

    // Esto controla que el horario sea de 30 a 30 minutos
    allowedStep: (m) => m % 30 === 0,

    // Funciones de Leaflet

    agregarMarker(event) {
      this.marker = latLng(event.latlng.lat, event.latlng.lng);
    },

    verEnMapa() {
      this.overlayMapa = true;
      var dataDir = encodeURI(
        this.direccion +
          ", Municipio " +
          this.select +
          ", Provincia de Buenos Aires, Argentina"
      );
      console.log(dataDir);
      fetch(
        "https://geocode.search.hereapi.com/v1/geocode?apikey=s0gvC3NKNulSUn6DSTyhf4jCcwVL5TN7C5oBELvwf3I&q=" +
          dataDir
      )
        .then((response) => response.json())
        .then((data) => {
          if (data["items"][0] === undefined) {
            this.resetAlertMapa = true;
            this.alertMapa = true;
            this.overlayMapa = false;
          }
          var lat = data["items"][0]["position"]["lat"];
          var lng = data["items"][0]["position"]["lng"];
          this.marker = latLng(lat, lng);
          this.overlayMapa = false;
          this.$refs.mapaCentro.mapObject.setView([lat, lng], 18);

          // Aca falta guardar los valores para la API
        });
    },
  },
};
</script>